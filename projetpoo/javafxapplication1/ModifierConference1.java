   private void displayConferenceDetails() {
    String query = "SELECT titre, president_nom, president_prenom, instit_org, lieu, topics, frais_inscription, date, date_limite_inscription FROM conference WHERE id_conf = " + conferenceId;
    try (Connection connection = DriverManager.getConnection(JDBC_URL, USERNAME, PASSWORD);
         Statement statement = connection.createStatement();
         ResultSet resultSet = statement.executeQuery(query)) {

        if (resultSet.next()) {
            String titre = resultSet.getString("titre");
            String presidentNom = resultSet.getString("president_nom");
            String presidentPrenom = resultSet.getString("president_prenom");
            String institOrg = resultSet.getString("instit_org");
            String lieu = resultSet.getString("lieu");
            String topics = resultSet.getString("topics");
            double fraisInscription = resultSet.getDouble("frais_inscription");
            Date date = resultSet.getDate("date");
            Date dateLimiteInscription = resultSet.getDate("date_limite_inscription");

            // Affectez ces valeurs aux champs correspondants dans votre interface utilisateur
            jTextField1.setText(titre);
            jTextField2.setText(presidentNom);
            jTextField3.setText(presidentPrenom);
            jTextField4.setText(institOrg);
            jTextField10.setText(lieu);
            jTextArea1.setText(topics);
            jTextField9.setText(String.valueOf(fraisInscription));
            jDateChooser1.setDate(date);
            jDateChooser2.setDate(dateLimiteInscription);
        } else {
            System.out.println("Aucune conférence trouvée avec l'ID: " + conferenceId);
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        // Gérer les exceptions
    }
}


 
  
