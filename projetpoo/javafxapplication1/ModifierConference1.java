    private boolean isNomPrenomValide(String nomPrenom) {
    return nomPrenom.matches("[a-zA-Z\\s]+");
}
private boolean isFraisValide(String frais) {
    return frais.matches("\\d+");
}

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(jTextField1.getText().isEmpty() || 
       jTextField2.getText().isEmpty() || 
       jTextField3.getText().isEmpty() || 
       jTextField4.getText().isEmpty() || 
       jTextField9.getText().isEmpty() || 
       jTextField10.getText().isEmpty() || 
       jTextArea1.getText().isEmpty() || 
       jDateChooser1.getDate() == null ||
       jDateChooser2.getDate() == null) {
        
        // Afficher un message d'erreur si un champ est vide
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
    } else {
        if(!isNomPrenomValide(jTextField2.getText()) || !isNomPrenomValide(jTextField3.getText())) {
            JOptionPane.showMessageDialog(this, "Le nom et le prénom doivent contenir uniquement des lettres et des espaces.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Vérifier si les frais sont valides
        if(!isFraisValide(jTextField9.getText())) {
            JOptionPane.showMessageDialog(this, "Les frais doivent contenir uniquement des chiffres.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }
//        int idConference = Integer.parseInt(jTextFieldIDConference.getText()); // Remplacez jTextFieldIDConference par le composant approprié qui affiche l'ID
        
        // Extraire les données des champs
        String titre = jTextField1.getText();
        String nomPresident = jTextField2.getText();
        String prenomPresident = jTextField3.getText();
        String institution = jTextField4.getText();
        String lieu = jTextField10.getText();
        String topics = jTextArea1.getText();
        double frais = Double.parseDouble(jTextField9.getText());
        Date dateDebut = jDateChooser1.getDate();
        Date dateLimite = jDateChooser2.getDate();
        
        // Mettre à jour les données dans la base de données
        // Utilisez votre connexion à la base de données et exécutez une requête SQL de mise à jour
        // Assurez-vous de gérer les exceptions SQL correctement
        
        try {
            Connection conn = DriverManager.getConnection(JDBC_URL, USERNAME, PASSWORD);
            String query = "UPDATE conference SET titre=?, president_nom=?, president_prenom=?, instit_org=?, lieu=?, topics=?, frais_inscription=?, date=?, date_limite_inscription=? WHERE id_conf=?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, titre);
            pstmt.setString(2, nomPresident);
            pstmt.setString(3, prenomPresident);
            pstmt.setString(4, institution);
            pstmt.setString(5, lieu);
            pstmt.setString(6, topics);
            pstmt.setDouble(7, frais);
            pstmt.setDate(8, new java.sql.Date(dateDebut.getTime()));
            pstmt.setDate(9, new java.sql.Date(dateLimite.getTime()));
            pstmt.setInt(10, conferenceId);
            
            int rowsUpdated = pstmt.executeUpdate();
            
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Les données de la conférence ont été mises à jour avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                AdminFonctionnalites NewWindow = new AdminFonctionnalites();
                NewWindow.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la mise à jour des données de la conférence.", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
            
            pstmt.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erreur lors de la mise à jour des données de la conférence.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

